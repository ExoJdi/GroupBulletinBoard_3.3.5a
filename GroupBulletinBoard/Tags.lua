local function Split(str, sep)
    sep = sep or "%s"
    local t = {}
    for w in string.gmatch(str, "([^" .. sep .. "]+)") do
        t[#t + 1] = w
    end
    return t
end

local function langSplit(src)
    local out = {}
    for lang, pat in pairs(src) do
        if pat ~= "-" then
            out[lang] = Split(pat, " ")
        end
    end
    return out
end

